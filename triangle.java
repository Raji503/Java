public class triangle
{
	public static void main(String args[])
	{	
		int i=0;
		while(i<=3)
		{
			if(i==0)
			{
			System.out.println("*");
			}
			else if(i==1)
			{
			System.out.println("*" +"\t" +"*");
			}
			else if(i==2)
			{
			System.out.println("*" +"\t" +"*" +"\t" +"*");
			}
			
		i++;
		}


	}
}